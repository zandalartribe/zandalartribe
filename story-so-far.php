        <h1>The story so far...</h1>
        
        <p>Following a lengthy campaign by dedicated RP-PvP enthusiasts to convince Blizzard that the Classic community needed servers of that type, the Zandalar Tribe server was announced for the EU region on August 9th, 2019.</p>

        <p>Many players, both old and new, registered their interest in the new server - a r/classicwow poll indicated that it would be the third-highest populated server, after the two pure PvP servers. Guilds began to plan their roster and raiding schedule; a new subreddit was created, and Discord communities formed around the Alliance and Horde factions.</p>

